class Gula < Formula
  desc "Instalador de componentes de gula"
  homepage "https://github.com/rudoapps/gula"
  url "https://github.com/rudoapps/homebrew-gula/archive/refs/tags/0.0.134.tar.gz"
  sha256 "58e06e87c36a981e84124775712b3c9da997237ada0fbb89b738d94fee786786"
  license "MIT"

  # Dependencias
  depends_on "ruby"
  depends_on "jq" # AÃ±ade jq como una dependencia
  
  def install
    bin.install "gula"
    (share/"support/scripts").install "scripts"
    
  end

  def post_install
    bin.install_symlink share/"support/scripts" => "gula-scripts"
  end

  test do
    system "#{bin}/gula", "install", "prueba"
  end
end
