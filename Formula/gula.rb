class Gula < Formula
  desc "Instalador de componentes de gula"
  homepage "https://github.com/rudoapps/gula"
  url "https://github.com/rudoapps/homebrew-gula/archive/refs/tags/0.0.68.tar.gz"
  sha256 "bb87518c41f478622c7bbfe022b0950727e8ef72795e751a26b402974604dd37"
  license "MIT"

  # Dependencias
  depends_on "ruby"
  depends_on "jq" # AÃ±ade jq como una dependencia

  def install
    bin.install "gula"
    (share/"support/scripts").install "scripts"
  end

  def post_install
    bin.install_symlink share/"support/scripts" => "gula-scripts"
  end

  test do
    system "#{bin}/gula", "install", "prueba"
  end
end
