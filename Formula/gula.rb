class Gula < Formula
  desc "Instalador de componentes de gula"
  homepage "https://github.com/rudoapps/gula"
  url "https://github.com/rudoapps/homebrew-gula/archive/refs/tags/0.0.115.tar.gz"
  sha256 "594d45c88cf317a7f6e7468abf8cda6b081c2c162b36b3b7ef47fbe86878d750"
  license "MIT"

  # Dependencias
  depends_on "ruby"
  depends_on "jq" # AÃ±ade jq como una dependencia
  
  def install
    bin.install "gula"
    (share/"support/scripts").install "scripts"
    
  end

  def post_install
    bin.install_symlink share/"support/scripts" => "gula-scripts"
  end

  test do
    system "#{bin}/gula", "install", "prueba"
  end
end
