class Gula < Formula
  desc "Instalador de componentes de gula"
  homepage "https://github.com/rudoapps/gula"
  url "https://github.com/rudoapps/homebrew-gula/archive/refs/tags/0.0.55.tar.gz"
  sha256 "781514e3902175ac60dd14e28e750aa9ea221982319d60b7f51e608cf13fe932"
  license "MIT"

  # Dependencias
  depends_on "ruby"
  depends_on "jq" # AÃ±ade jq como una dependencia

  def install
    bin.install "gula"
    (share/"support/scripts").install "scripts"
  end

  def post_install
    bin.install_symlink share/"support/scripts" => "gula-scripts"
  end

  test do
    system "#{bin}/gula", "install", "prueba"
  end
end
